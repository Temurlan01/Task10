# 7. Поиск отрицательного числа в списке:
# Программа использует цикл while, чтобы проверить, есть ли в списке
# отрицательное число. Если находится отрицательное число, программа выводит сообщение о его наличии, в противном случае выводится сообщение об его отсутствии.

my_number = [10, 20, -30, 40]
maxium=0
while True:
    for number in my_number > maxium:
        print("В списке нет отрицательных чисил")

    else:
        print("В списке есть отрицательное число")
